class Standing {
  final int rank;
  final String teamName;
  final String teamLogo;
  final int points;
  final int matchesPlayed;
  final int win;
  final int draw;
  final int lose;

  Standing({
    required this.rank,
    required this.teamName,
    required this.teamLogo,
    required this.points,
    required this.matchesPlayed,
    required this.win,
    required this.draw,
    required this.lose,
  });
}
